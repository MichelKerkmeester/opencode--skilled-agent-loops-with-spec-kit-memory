#!/usr/bin/env node
'use strict';
// Synthesis step: compile findings-registry.json into the reuse-catalog-first
// context report (md + json). Curation rules: agreement-eligible entries first,
// then high-relevance singles; contradictions and refutations surface in Gaps.

const fs = require('fs');
const path = require('path');

function main() {
  const [, , contextDir] = process.argv;
  const reg = JSON.parse(fs.readFileSync(path.join(contextDir, 'findings-registry.json'), 'utf8'));
  const m = reg.metrics || {};

  const pick = (cat, cap, minRel) => (reg[cat] || [])
    .filter((u) => u.agreementEligible || (u.relevance || 0) >= minRel)
    .sort((a, b) => (b.agreementEligible - a.agreementEligible) || ((b.relevance || 0) - (a.relevance || 0)))
    .slice(0, cap);

  const fmt = (u) => ({
    path: u.path, symbol: u.symbol, kind: u.kind, signature: u.signature || '',
    reuse: u.reuse || '', evidence: u.evidence || '', relevance: u.relevance || 0,
    agreement: `${u.agreement}/3`, eligible: !!u.agreementEligible,
    freshness: u.agreementEligible ? 'seat-verified' : 'unverified',
    notes: (u.notes || '').slice(0, 220),
  });

  const reuse = pick('reuseCandidates', 28, 0.75).map(fmt);
  const integration = pick('integrationPoints', 30, 0.85).map(fmt);
  const conventions = pick('conventions', 14, 0.7).map(fmt);
  const dependencies = pick('dependencies', 22, 0.85).map(fmt);
  const gapsRaw = (reg.gaps || []).sort((a, b) => (b.relevance || 0) - (a.relevance || 0));
  const refuted = gapsRaw.filter((u) => /REFUTED/i.test(u.notes || '')).slice(0, 20).map(fmt);
  const gaps = gapsRaw.filter((u) => !/REFUTED/i.test(u.notes || '')).slice(0, 20).map(fmt);

  // Touch list: files ranked by count of high-relevance units
  const fileScore = new Map();
  for (const cat of ['reuseCandidates', 'integrationPoints', 'dependencies', 'gaps'])
    for (const u of reg[cat] || []) {
      if ((u.relevance || 0) < 0.8) continue;
      fileScore.set(u.path, (fileScore.get(u.path) || 0) + (u.agreementEligible ? 2 : 1));
    }
  const touchList = [...fileScore.entries()].sort((a, b) => b[1] - a[1]).slice(0, 20);

  const row = (u) => `| \`${u.path.split('/').slice(-2).join('/')}\`::\`${u.symbol}\` | \`${(u.signature || '').replace(/\|/g, '\\|').slice(0, 80)}\` | ${u.reuse || '-'} | ${u.relevance} | ${u.agreement} | ${u.freshness} | ${u.notes.replace(/\|/g, '\\|').slice(0, 150)} |`;

  const md = `# Context Report: SQLite surfaces → Turso/libSQL migration touchpoints

> Generated by \`deep-context\`. Ships verified **pointers**, not source bodies —
> open the cited \`file:symbol\` to read code just-in-time. Sections are ordered
> by value-per-token. Each finding carries confidence (cross-seat agreement)
> and freshness (seat-verified via the iteration-9 re-confirmation pass vs unverified).

**Scope**: SQLite usage surfaces across system-spec-kit mcp_server, system-code-graph, and system-skill-advisor (better-sqlite3 + sqlite-vec + FTS5, recursive CTEs, pragmas, WAL, daemon/lease single-writer models) plus the active release packet's storage changes, oriented at Turso/libSQL migration.
**Executors**: 3 × cli-opencode xiaomi/mimo-v2.5-pro --variant high (COSTAR; perspectives: structure / write-paths / migration-risk)
**Iterations**: 10 (8 frontier slices + 1 re-confirmation pass + 1 coverage sweep)
**Stop**: cap (maxIterationsReached) — graph blocker \`low_cross_executor_agreement\` open at stop (structural for a perspective-differentiated single-model pool; see Methodology)

---

## 1. REUSE Catalog (highest value)

| symbol | signature | reuse | confidence | agree | freshness | notes |
|--------|-----------|-------|------------|-------|-----------|-------|
${reuse.map(row).join('\n')}

## 2. Integration Points

${integration.map((u) => `- **[${u.relevance >= 0.9 ? 'HARD' : 'soft'}]** \`${u.path}\`::\`${u.symbol}\` (${u.evidence || 'evidence in registry'}) — ${u.notes} [agree ${u.agreement}, ${u.freshness}]`).join('\n')}

## 3. Touch List

${touchList.map(([f, s], i) => `${i + 1}. \`${f}\` — migration-relevant unit weight ${s}`).join('\n')}

## 4. Conventions

${conventions.map((u) => `- \`${u.path}\`::\`${u.symbol}\` — ${u.notes} [${u.freshness}]`).join('\n')}

## 5. Pruned Dependency Subgraph

${dependencies.map((u) => `- \`${u.path}\` → \`${u.symbol}\` (${u.kind}) — ${u.notes} [agree ${u.agreement}]`).join('\n')}

## 6. Prior Art / Decisions

- **[v0.5.0 baseline]** Research docs 001–003 in \`research/\` — 16-gap catalog, paths A/B/C, P0–P4 priorities. Frozen comparison baseline for the deep-research revalidation.
- **[2026-06]** Packet 027-xce-research-based-refinement — incremental-index schema v31–v34, causal tombstones, statediff reconciliation, OpenLTM observability (all post-baseline storage surface; mapped in iterations 4 and covered above).
- **[vendored]** \`external/turso-main\` at v0.7.0-pre.6 — the revalidation comparison target.

## 7. Gaps & Unknowns

### Open gaps
${gaps.map((u) => `- \`${u.path}\`::\`${u.symbol}\` — ${u.notes} [agree ${u.agreement}]`).join('\n')}

### Refuted by the re-confirmation pass (iteration 9)
${refuted.map((u) => `- \`${u.path}\`::\`${u.symbol}\` — ${u.notes}`).join('\n')}

---

## Methodology & Confidence

- Per-seat attribution + agreement counts; relevance gate 0.55; contradictions surfaced, never auto-resolved (${m.findings} total registry findings, ${reg.contradictions ? reg.contradictions.length : 'n/a'} contradiction pairs recorded).
- **Single-model pool disclosure**: all three seats ran xiaomi/mimo-v2.5-pro with differentiated analytical perspectives (structure / write-paths / migration-risk). Agreement here means cross-SEAT confirmation, not cross-MODEL confirmation — weaker than a heterogeneous pool; the iteration-9 same-objective re-confirmation pass partially compensates (36 high-value units independently verified by all 3 seats).
- Convergence signals at stop: sliceCoverage 1.0, reuseCatalogCoverage 1.0, agreementRate ~0.19 (gated cohort), relevanceFloor 1.0, dependencyCompleteness 1.0; blended graph score 0.811.
- Stop decision: cap (maxIterationsReached at 10). Blocking gate \`low_cross_executor_agreement\` remained open — structural consequence of perspective differentiation; ${m.agreementEligible} of ${m.findings} findings are agreement-eligible.
- Code-graph verification unavailable during the run (daemon down) — frontier seeded via Glob+Grep fallback; freshness label \`seat-verified\` = confirmed by ≥2 seats or the iteration-9 verification pass, \`unverified\` = single-seat claim.
`;

  fs.writeFileSync(path.join(contextDir, 'context-report.md'), md);
  fs.writeFileSync(path.join(contextDir, 'context-report.json'), JSON.stringify({
    scope: 'SQLite surfaces across system-spec-kit, system-code-graph, system-skill-advisor + 027 deltas, oriented at Turso/libSQL migration',
    executors: ['mimo-a', 'mimo-b', 'mimo-c'],
    model: 'xiaomi/mimo-v2.5-pro',
    iterations: m.iterationsCompleted,
    stop: 'maxIterationsReached',
    metrics: m,
    reuseCatalog: reuse, integrationPoints: integration, conventions, dependencies,
    touchList: touchList.map(([f, s]) => ({ file: f, weight: s })),
    gaps, refuted,
  }, null, 2));
  console.log(JSON.stringify({ report: 'context-report.md', json: 'context-report.json', reuse: reuse.length, integration: integration.length, dependencies: dependencies.length, gaps: gaps.length, refuted: refuted.length, touchFiles: touchList.length }));
}

main();
