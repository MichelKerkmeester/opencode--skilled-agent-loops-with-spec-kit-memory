#!/usr/bin/env node
'use strict';
/**
 * Manual authoring swarm for the deep-loop recommendations implementation-planning tree.
 *
 * Dispatches one agent per phase-folder in the work-list, workspace-write,
 * each mirroring a real validated phase leaf as its structural template.
 * After an agent returns, the driver deterministically
 * generates description.json + graph-metadata.json and runs strict validation
 * on that folder, recording pass/fail. Operator-authorized parallel dispatch
 * (rule-15 swarm exception); concurrency-capped to be gentle on the shared OAuth.
 *
 * Usage: node author-swarm.cjs --wave <N> [--concurrency <k>] [--executor codex|opencode|fallback] [--only <id[,id]>] [--dry-run]
 */
const fs = require('fs');
const path = require('path');
const { spawn, spawnSync } = require('child_process');

const ROOT = '/Users/michelkerkmeester/MEGA/Development/Code_Environment/Public';
const PACKET_ROOT = path.join(ROOT, '.opencode/specs/system-deep-loop/065-deep-loop-innovation');
const SCRATCH = path.join(PACKET_ROOT, 'scratch');
const WORKLIST = path.join(SCRATCH, 'author-worklist.json');
const LOGDIR = path.join(SCRATCH, 'swarm-logs');
const RESULTS = path.join(SCRATCH, 'swarm-results.jsonl');

// Two executors. Primary cli-codex (SOL): preferred authoring quality, but shares one ChatGPT
// OAuth that gets contended/killed under load from concurrent sessions. Fallback cli-opencode
// (LUNA): the paid OpenAI API — a separate credential that keeps working when codex errors out.
// Operator policy: codex first, opencode when codex errors. The --executor flag in main()
// overrides EXECUTOR_CHAIN: 'fallback' (default) tries both; 'codex'/'opencode' force one lane.
const CODEX = { kind: 'codex', model: 'gpt-5.6-sol', effort: 'high' };
const OPENCODE = { kind: 'opencode', model: 'openai/gpt-5.6-luna-fast', variant: 'xhigh' };
let EXECUTOR_CHAIN = [CODEX, OPENCODE];
const TIMEOUT_MS = 1500 * 1000; // 25 min per agent (author + optional self-validate)

const GEN_DESC = path.join(ROOT, '.opencode/skills/system-spec-kit/scripts/dist/spec-folder/generate-description.js');
const BACKFILL = path.join(ROOT, '.opencode/skills/system-spec-kit/scripts/dist/graph/backfill-graph-metadata.js');
const VALIDATE = path.join(ROOT, '.opencode/skills/system-spec-kit/scripts/spec/validate.sh');

function nowStamp() { return new Date().toISOString(); }
function loadJSON(p, f) { try { return JSON.parse(fs.readFileSync(p, 'utf8')); } catch { return f; } }

