// ---------- dispatch one agent (async, with executor fallback) ----------
// A folder is authored when its doc set exists (leaf = the 4 md files; parent = spec.md only).
function folderAuthored(item) {
  const abs = path.join(ROOT, item.folder);
  if (!fs.existsSync(path.join(abs, 'spec.md'))) return false;
  if (item.kind === 'parent') return true;
  return ['plan.md', 'tasks.md', 'checklist.md'].every((f) => fs.existsSync(path.join(abs, f)));
}

function runExecutor(item, exec, logFd) {
  return new Promise((resolve) => {
    const brief = buildBrief(item);
    let cmd, args, env;
    if (exec.kind === 'codex') {
      cmd = 'codex';
      args = ['exec', '-m', exec.model, '-c', 'model_reasoning_effort=' + exec.effort,
        '-c', 'service_tier=fast', '-c', 'approval_policy=never', '--sandbox', 'workspace-write', brief];
      env = { ...process.env, AI_SESSION_CHILD: '1' };
    } else {
      cmd = 'opencode';
      args = ['run', '--model', exec.model, '--variant', exec.variant, '--format', 'json', '--dir', ROOT, brief];
      // 'ignore' stdin = /dev/null (opencode hangs without closed stdin); MK_SPEC_GATE_ENFORCE=0
      // stops the child inheriting an enforced spec-gate; AI_SESSION_CHILD=1 shares the worktree.
      env = { ...process.env, AI_SESSION_CHILD: '1', MK_SPEC_GATE_ENFORCE: '0' };
    }
    const t0 = Date.now();
    const child = spawn(cmd, args, { cwd: ROOT, stdio: ['ignore', logFd, logFd], env });
    const timer = setTimeout(() => { try { child.kill('SIGKILL'); } catch {} }, TIMEOUT_MS);
    child.on('close', (code) => { clearTimeout(timer); resolve({ kind: exec.kind, code, ms: Date.now() - t0 }); });
    child.on('error', (e) => { clearTimeout(timer); resolve({ kind: exec.kind, code: -1, err: String(e && e.message), ms: Date.now() - t0 }); });
  });
}

// Try each executor in EXECUTOR_CHAIN until the folder is authored, so a codex error/kill
// falls through to opencode without losing the folder.
async function dispatchOne(item) {
  fs.mkdirSync(LOGDIR, { recursive: true });
  fs.mkdirSync(path.join(ROOT, item.folder), { recursive: true });
  const log = path.join(LOGDIR, `author-${item.id}.log`);
  const out = fs.openSync(log, 'w');
  const attempts = [];
  let totalMs = 0, lastCode = -1;
  for (let i = 0; i < EXECUTOR_CHAIN.length; i++) {
    const r = await runExecutor(item, EXECUTOR_CHAIN[i], out);
    attempts.push(EXECUTOR_CHAIN[i].kind + ':' + r.code);
    totalMs += r.ms; lastCode = r.code;
    if (r.code === 0 && folderAuthored(item)) break;
    if (i < EXECUTOR_CHAIN.length - 1) {
      fs.writeSync(out, `\n\n===== ${EXECUTOR_CHAIN[i].kind} did not author the folder (code=${r.code}); falling back to ${EXECUTOR_CHAIN[i + 1].kind} =====\n\n`);
    }
  }
  try { fs.closeSync(out); } catch {}
  return { id: item.id, folder: item.folder, code: folderAuthored(item) ? 0 : lastCode, ms: totalMs, attempts, log };
}

// ---------- deterministic post-processing (metadata + validate) ----------
function sh(cmd, args) {
  const r = spawnSync(cmd, args, { cwd: ROOT, encoding: 'utf8', timeout: 180 * 1000, maxBuffer: 32 * 1024 * 1024 });
  return { status: r.status, out: (r.stdout || '') + (r.stderr || '') };
}
function postProcess(item) {
  const abs = path.join(ROOT, item.folder);
  const steps = {};
  // generate-description.js requires TWO positionals: <folder-path> <base-path>.
  // base-path is the repo root against which the folder's canonical identity is resolved.
  steps.gen_desc = sh('node', [GEN_DESC, abs, ROOT, '--level', String(item.level)]).status;
  // Re-derive the graph-metadata fingerprint AFTER description.json exists so the
  // source-doc set is complete when the integrity hash is computed.
  steps.backfill = sh('node', [BACKFILL, abs]).status;
  const v = sh('bash', [VALIDATE, item.folder, '--strict']);
  const tail = v.out.split('\n').filter((l) => /Errors:|Warnings:|RESULT:|Summary:/.test(l)).join(' | ');
  steps.validate_status = v.status;
  steps.validate_tail = tail;
  // Exit 0 = fully clean under --strict (no errors AND no warnings, across the folder
  // and — for a parent — its recursed children). A loose /PASSED/ match would
  // false-positive on a child's PASSED line while the parent itself FAILED on a warning.
  steps.pass = (v.status === 0);
  return steps;
}

// ---------- concurrency pool ----------
async function pool(items, n, fn) {
  const results = [];
  let i = 0;
  const workers = Array.from({ length: Math.min(n, items.length) }, async () => {
    while (i < items.length) {
      const idx = i++;
      results[idx] = await fn(items[idx]);
    }
  });
  await Promise.all(workers);
  return results;
}

// ---------- main ----------
async function main() {
  const argv = process.argv.slice(2);
  const wave = parseInt((argv[argv.indexOf('--wave') + 1] || '1'), 10);
  const concurrency = parseInt((argv[argv.indexOf('--concurrency') + 1] || '2'), 10);
  const dryRun = argv.includes('--dry-run');
  const executorMode = argv.indexOf('--executor') >= 0 ? argv[argv.indexOf('--executor') + 1] : 'fallback';
  const onlyArg = argv.indexOf('--only') >= 0 ? argv[argv.indexOf('--only') + 1] : null;
  const onlyIds = onlyArg ? new Set(onlyArg.split(',').map((s) => s.trim())) : null;
  EXECUTOR_CHAIN = executorMode === 'codex' ? [CODEX]
    : executorMode === 'opencode' ? [OPENCODE]
    : [CODEX, OPENCODE]; // fallback (default): codex first, opencode when codex errors out

  const wl = loadJSON(WORKLIST, null);
  if (!wl || !Array.isArray(wl.items)) { console.error('FATAL: bad worklist'); process.exit(2); }
  let items = wl.items.filter((x) => x.wave === wave && !x.done);
  // --only narrows both dispatch AND post-process to the named ids, so re-authoring a
  // single failed folder never rewrites its already-validated siblings' metadata.
  if (onlyIds) items = items.filter((x) => onlyIds.has(String(x.id)));
  if (!items.length) { console.error(`No pending items for wave ${wave}${onlyIds ? ` matching --only ${onlyArg}` : ''}`); process.exit(2); }

  console.log(`Swarm wave ${wave}: ${items.length} folders, concurrency ${concurrency}, executor chain [${EXECUTOR_CHAIN.map((e) => e.kind).join(' -> ')}]`);
  if (dryRun) {
    for (const it of items) { console.log(`\n===== ${it.id} (${it.folder}) =====\n`); console.log(buildBrief(it).slice(0, 1400) + '\n…[truncated]'); }
    return;
  }

  // Resume support: skip re-dispatching already-authored folders (folderAuthored) after an
  // interrupted run, but still post-process every wave item below.
  const toAuthor = items.filter((it) => !folderAuthored(it));
  const already = items.filter(folderAuthored);
  if (already.length) console.log(`Resume: ${already.length} already-authored, skipping re-dispatch: ${already.map((x) => x.id).join(', ')}`);

  console.log(`[${nowStamp()}] dispatching ${toAuthor.length} agents…`);
  const dispatched = toAuthor.length ? await pool(toAuthor, concurrency, dispatchOne) : [];
  console.log(`[${nowStamp()}] agents returned; post-processing ${items.length} folders (metadata + validate)…`);

  // Post-process leaves before parents: a phase-parent only validates AS a parent
  // once ≥1 child folder exists, and backfill derives children_ids from present children.
  const ordered = [...items].sort((a, b) => (a.kind === 'parent' ? 1 : 0) - (b.kind === 'parent' ? 1 : 0));
  for (const item of ordered) {
    const d = dispatched.find((x) => x.id === item.id) || { code: 'skip', ms: 0, attempts: ['skip'] };
    const post = postProcess(item);
    const row = { id: item.id, folder: item.folder, agent_code: d.code, agent_attempts: d.attempts || [], agent_ms: d.ms, ...post, ts: nowStamp() };
    fs.appendFileSync(RESULTS, JSON.stringify(row) + '\n');
    console.log(`  ${item.id}: [${(d.attempts || ['skip']).join(',')}] (${Math.round(d.ms / 1000)}s) | validate: ${post.validate_tail || '(none)'} | PASS=${post.pass}`);
  }
  console.log(`\nWave ${wave} done. Results: ${RESULTS} | logs: ${LOGDIR}`);
}

main();
